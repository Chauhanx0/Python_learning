#Keywords vs Identifiers

# Keywords

# Reserved words in Python that have a specific meaning.
# Cannot be used as variable names.
# Examples: if, else, for, while
# Part of the Python syntax.
# They cannot be redefined or changed.



# Identifiers

# Names given to variables, functions, classes, etc.
# Can be used as variable names if not a keyword.
# Examples: x, number, sum, result
# User-defined, meaningful names in the code.
# Can be defined and redefined by the programmer.



#Variables vs Keywords

# Variables

# Used to store data.
# Can be created, modified, and deleted by the programmer.
# Examples: x, age, name
# Hold values that are manipulated in the program.
# Variable names must follow naming rules but are otherwise flexible.


# Keywords

# Reserved words with predefined meanings in Python.
# Cannot be modified or used as variable names.
# Examples: if, while, for
# Used to define the structure of Python code.
# Fixed by Python language and cannot be altered.

